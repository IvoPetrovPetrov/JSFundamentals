//Relational or comparison operators
// > more than
// < less than
// >= more than equal
// <= less than equal
var result = 10 > 5
console.log(result)

var result1 = 10 < 5
console.log(result1)

var result2 = 10 >= 5
console.log(result2)

var result3 = 10 <= 5
console.log(result3)

var result4 = 10 <= 10
console.log(result4)

//equality operators
var x = 1
console.log(x == '1') // compare the value - left vs right
console.log(x === '1') // strict comparison - value and data type
console.log(x === 1)